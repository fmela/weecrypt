mp_digit
mp_sieve(const mp_digit *u, mp_size size, unsigned nprimes)
{
	MP_NORMALIZE(u, size);
	if (!size)
		return 0;
	if ((u[0] & 1) == 0)
		return (u[0] == 2) ? 0 : 2;

	if (nprimes == 0 || nprimes > NPRIMES)
		nprimes = NPRIMES;

	mp_digit p = 1;
	for (unsigned i = 0; i < nprimes; i++) {
		p += (mp_digit)prime_offsets[i] * 2;
		if (size == 1 && u[0] <= p)
			break;
		if (mp_dmod(u, size, p) == 0)
			return p;
	}
	return 0;
}
