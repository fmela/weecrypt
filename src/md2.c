void
md2_init(md2_context *ctx)
{
	ASSERT(ctx != NULL);

	memset(ctx, 0, sizeof(*ctx));
}

void
md2_update(md2_context *ctx, const void *input, unsigned len)
{
	unsigned		i, idx, plen;
	const unsigned char	*data = (const unsigned char *)input;

	ASSERT(ctx != NULL);
	ASSERT(input != NULL);
	ASSERT(len > 0);

	idx = ctx->len;
	ctx->len = (idx + len) & 0xf;

	plen = 16 - idx;
	if (len >= plen) {
		memcpy(&ctx->buf[idx], data, plen);
		md2_step(ctx->md2, ctx->cksum, ctx->buf);
		for (i = plen; i + 15 < len; i += 16)
			md2_step(ctx->md2, ctx->cksum, &data[i]);
		idx = 0;
	} else {
		i = 0;
	}

	memcpy(&ctx->buf[idx], &data[i], len - i);
}

void
md2_final(md2_context *ctx, void *digest)
{
	unsigned	idx, plen;

	ASSERT(ctx != NULL);
	ASSERT(digest != NULL);

	idx = ctx->len;
	plen = 16 - idx;
	md2_update(ctx, MD2_Padding[plen], plen);
	md2_update(ctx, ctx->cksum, 16);
	memcpy(digest, ctx->md2, 16);
	memset(ctx, 0, sizeof(*ctx));
}

static void
md2_step(unsigned char *md2, unsigned char *cksum, const unsigned char *data)
{
	unsigned int	i, j, t;
	unsigned char	x[48];

	ASSERT(md2 != NULL);
	ASSERT(cksum != NULL);
	ASSERT(data != NULL);

	memcpy(x, md2, 16);
	memcpy(x + 16, data, 16);
	for (i = 0; i < 16; i++)
		x[i | 32] = md2[i] ^ data[i];

	t = 0;
	for (i = 0; i < 18; i++) {
		for (j = 0; j < 48; j++)
			t = x[j] ^= pi_table[t];
		t += i;
		t &= 0xff;
	}

	memcpy(md2, x, 16);
	t = cksum[15];
	for (i = 0; i < 16; i++)
		t = cksum[i] ^= pi_table[data[i] ^ t];

	memset(x, 0, sizeof(x));
}

void
md2_hash(md2_context *ctx, const void *input, unsigned len, void *digest)
{
	ASSERT(ctx != NULL);
	ASSERT(input != NULL);
	ASSERT(len > 0);
	ASSERT(digest != NULL);

	md2_init(ctx);
	md2_update(ctx, input, len);
	md2_final(ctx, digest);
}
